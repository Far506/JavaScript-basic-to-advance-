var marks = prompt("Enter your marks: ");

if (marks >= 90)
    console.log("A+");
else if (marks >= 85)
    console.log("A");
else if (marks >= 80)
    console.log("B+");
else if (marks >= 75)
    console.log("B");
else if (marks >= 70)
    console.log("C+");
else if (marks >= 65)
    console.log("C");
else if (marks >= 60)
    console.log("D+");
else if (marks >= 50)
    console.log("D")
else
    console.log("F");